    @staticmethod
    def create_license_file(folder_path, license_key, author_name=None, year=None):
        """
        Create a LICENSE file in the specified folder.
        
        Args:
            folder_path: Path to the folder
            license_key: Key of the license to create
            author_name: Name of the copyright holder
            year: Year for the copyright
            
        Returns:
            bool: True if license file was created
        """
        from pathlib import Path
        
        if license_key == 'None':
            return True
        
        license_content = LicenseTemplates.get_license_content(license_key, author_name, year)
        
        if license_content is None:
            print(f"Warning: License '{license_key}' not found.")
            return False
        
        license_path = Path(folder_path) / 'LICENSE'
        
        if license_path.exists():
            return True
        
        license_path.write_text(license_content)
        print(f"Created LICENSE ({LicenseTemplates.AVAILABLE_LICENSES.get(license_key)})")
        
        return True
